package br.univille;

public class Calculadora {
    
    public int soma(int num1, int num2){
        return num1 + num2;
    }

    public int subtrai(int num1, int num2){
        return num1 - num2;
    }

    public int multiplicacao(int num1, int num2) {
        return num1 * num2;
    }

    public float divisao(int i, int j) {
        return i / j;
    }

}
